package com.simongong.extendedtype;

public class TreeLinkNode {
    public int val;
    public TreeLinkNode left;
    public TreeLinkNode right;
    public TreeLinkNode next;

    public TreeLinkNode(int x) {
        this.val = x;
        this.left = null;
        this.right = null;
        this.next = null;
    }
}
